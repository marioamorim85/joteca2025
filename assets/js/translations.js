let currentLanguage = 'pt';

function translatePage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    
    // Atualiza o texto do botão (mostra o idioma oposto ao atual)
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        const oppositeLang = lang === 'pt' ? 'EN' : 'PT';
        languageToggle.querySelector('span').textContent = oppositeLang;
    }
    
    // Atualiza o atributo lang do HTML
    document.documentElement.lang = lang;
    
    // Atualiza o título da página
    const titleElement = document.querySelector('title[data-translate]');
    if (titleElement) {
        const titleKey = titleElement.getAttribute('data-translate');
        if (translations[lang][titleKey]) {
            document.title = translations[lang][titleKey];
            titleElement.textContent = translations[lang][titleKey];
        }
    }
    
    // Traduz todos os elementos com data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            if (element.tagName === 'TITLE') {
                document.title = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Atualiza o texto do processo de avaliação
    const evaluationDescription = document.getElementById('evaluation-description');
    if (evaluationDescription) {
        evaluationDescription.innerHTML = translations[lang]['submissions.evaluation.description'];
    }
}

// Inicializa a tradução quando a página carrega
document.addEventListener('DOMContentLoaded', () => {
    // Força o idioma português na inicialização
    translatePage('pt');
    
    // Adiciona o evento de clique ao botão de idioma
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', () => {
            const newLang = currentLanguage === 'pt' ? 'en' : 'pt';
            translatePage(newLang);
        });
    }
}); 